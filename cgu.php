		Tout d’abord, qu’est-ce que le RGPD ?

		Le sigle RGPD signifie Règlement Général sur la Protection des Données. Le RGPD encadre le traitement des données personnelles sur le territoire de l’Union européenne. Tout organisme, quels que soient sa taille, son pays d’implantation et son activité, peut être concerné.  Ces données personnelles peuvent comporter  des informations se rapportant à une personne physique identifiée ou identifiable  (Nom, Prénom,  ge, adresse etc.…).


		Les contraintes du RGPD :	

		le RGPD s’applique à toute organisation, publique et privée, qui traite des données personnelles pour son compte ou non, dès lors :
		
		- Qu'elle est établie sur le territoire de l’Union européenne,
		
		- Que son activité cible directement des résidents européens.

		Cependant ce n’est pas un problème pour nous mais pour respecter cette nouvelle réglementation, les TPE-PME devront se plier à des actions telles que :
		- La mise en conformité contractuelle et juridique.

		- La gestion du statut de "sous-traitant" s'il est constaté, et ce afin de rassurer les clients concernés.

		- La mise en place de toutes les mesures techniques et organisationnelles visant la protection des données à caractère personnel.

		- La nomination d'un délégué à la protection des données personnelles (Data Protection Officer). Cette fonction peut être externalisée ou mutualisée. 

		- La mise en place d'une gouvernance adaptée (procédures et rôles).

		- La gestion récurrente du plan de mise en conformité. 


		Mentions légales :
		Tous les sites internet, qu'ils soient édités à titre professionnel ou à titre non professionnel, doivent afficher des mentions obligatoires pour l'information du public. Le non-respect de ces obligations est sanctionné.

		Les données  récolter par le site : toutes les données transmise lors du renseignement de la fiche de fais et de la connexion.

